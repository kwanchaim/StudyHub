// ─── โจทย์คำนวณ ──────────────────────────────────────────────────
export function generateMathProblems(difficulty: GameDifficulty): MathProblem[] {
  const problems: MathProblem[] = [];
  for (let i = 0; i < 20; i++) {
    if (difficulty === "easy") {
      const ops = ["+", "-", "×"] as const;
      const op = ops[Math.floor(Math.random() * ops.length)];
      let a: number, b: number, answer: number;
      if (op === "+") { a = Math.floor(Math.random() * 20) + 1; b = Math.floor(Math.random() * 20) + 1; answer = a + b; }
      else if (op === "-") { a = Math.floor(Math.random() * 20) + 10; b = Math.floor(Math.random() * a); answer = a - b; }
      else { a = Math.floor(Math.random() * 9) + 1; b = Math.floor(Math.random() * 9) + 1; answer = a * b; }
      problems.push({ question: `${a} ${op} ${b} = ?`, answer, difficulty });
    } else if (difficulty === "medium") {
      const ops = ["+", "-", "×", "÷"] as const;
      const op = ops[Math.floor(Math.random() * ops.length)];
      let a: number, b: number, answer: number;
      if (op === "+") { a = Math.floor(Math.random() * 100) + 1; b = Math.floor(Math.random() * 100) + 1; answer = a + b; }
      else if (op === "-") { a = Math.floor(Math.random() * 100) + 50; b = Math.floor(Math.random() * 50) + 1; answer = a - b; }
      else if (op === "×") { a = Math.floor(Math.random() * 15) + 2; b = Math.floor(Math.random() * 15) + 2; answer = a * b; }
      else { b = Math.floor(Math.random() * 10) + 2; answer = Math.floor(Math.random() * 20) + 1; a = b * answer; }
      problems.push({ question: `${a} ${op} ${b} = ?`, answer, difficulty });
    } else {
      const types = ["sq", "perc", "mul", "div"] as const;
      const t = types[Math.floor(Math.random() * types.length)];
      let question: string, answer: number;
      if (t === "sq") { const n = Math.floor(Math.random() * 12) + 2; question = `${n}² = ?`; answer = n * n; }
      else if (t === "perc") { const pct = [10, 20, 25, 50][Math.floor(Math.random() * 4)]; const n = (Math.floor(Math.random() * 8) + 1) * 20; question = `${pct}% of ${n} = ?`; answer = (pct * n) / 100; }
      else if (t === "mul") { const a = Math.floor(Math.random() * 25) + 10; const b = Math.floor(Math.random() * 25) + 10; question = `${a} × ${b} = ?`; answer = a * b; }
      else { const b = Math.floor(Math.random() * 12) + 2; const ans = Math.floor(Math.random() * 20) + 2; question = `${b * ans} ÷ ${b} = ?`; answer = ans; }
      problems.push({ question, answer, difficulty });
    }
  }
  return problems;
}
